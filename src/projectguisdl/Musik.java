    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        Lagu[] urut = new Lagu[daftarLagu.getSize()];
        int in = 0;

        ListNodeObject bantu = daftarLagu.getHead().next;
        while (bantu != daftarLagu.getHead()) {
            urut[in] = bantu.getElemen();
            in++;
            bantu = bantu.getNext();
        }

        String cari = JOptionPane.showInputDialog("Ketikkan lagu yang dicari :");
        Lagu kunci = new Lagu(cari);
        int hasil = Larik.binarySearch(urut, kunci);
        if (hasil == -1) {
            JOptionPane.showMessageDialog(null, "Lagu tidak terdaftar");
        } else {
            JOptionPane.showMessageDialog(null, "Lagu berada di urutan ke :" + hasil+ " dari isi playlist");
        }
    }//GEN-LAST:event_SearchActionPerformed

