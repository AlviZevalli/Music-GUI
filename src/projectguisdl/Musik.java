    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        String[] m = {"Hapus Awal", "Hapus Akhir"};
        int x = JOptionPane.showOptionDialog(null, "Urutan mana yang akan di hapus ?",
                "Click a button",
                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, m, m[0]);

        if (x == 0) {
            daftarLagu.removeFirst();
            JOptionPane.showMessageDialog(null, "Lagu pada urutan awal terhapus");
        }
        if (x == 1) {
            daftarLagu.removeLast();
            JOptionPane.showMessageDialog(null, "Lagu pada urutan akhir terhapus");
        }
    }//GEN-LAST:event_HapusActionPerformed

